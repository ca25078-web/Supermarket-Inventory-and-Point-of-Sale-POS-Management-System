    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    String name = txtName.getText().trim();
    if (!name.isEmpty()) {
        if (controller.addCategory(name)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Category Added!");
            refreshCategoryTable();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error adding category.");
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnAddActionPerformed

    private void categoryTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_categoryTableMouseClicked
    int selectedRow = categoryTable.getSelectedRow();
    if (selectedRow >= 0) {
        selectedCategoryId = (int) categoryTable.getValueAt(selectedRow, 0);
        txtName.setText(categoryTable.getValueAt(selectedRow, 1).toString());
    }        // TODO add your handling code here:
    }//GEN-LAST:event_categoryTableMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
    String name = txtName.getText().trim();
    if (selectedCategoryId != -1 && !name.isEmpty()) {
        if (controller.updateCategory(selectedCategoryId, name)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Category Updated!");
            refreshCategoryTable();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error updating category.");
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a category from the table first.");
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    if (selectedCategoryId != -1) {
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this category?", "Warning", javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            if (controller.deleteCategory(selectedCategoryId)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Category Deleted!");
                refreshCategoryTable();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Cannot delete category (It might be in use by products!).");
            }
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a category to delete.");
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

