    // --- CORE LOGIC: Refresh Table ---
    private void refreshCategoryTable() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) categoryTable.getModel();
        model.setRowCount(0); // Clear old data
        
        java.util.List<Object[]> list = controller.getAllCategories();
        for (Object[] row : list) {
            model.addRow(row);
        }
        
        // Reset the form
        txtName.setText("");
        selectedCategoryId = -1;
    }

