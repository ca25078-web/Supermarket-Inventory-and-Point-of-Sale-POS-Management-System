    private void btnSaveThresholdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveThresholdActionPerformed
// 1. Get the inputs from your UI
        // Change 'cmbApplyTo', 'txtTargetID', and 'txtNewThreshold' to match your actual variable names!
        String updateType = cmbApplyTo.getSelectedItem().toString();
        String targetIdStr = txtTargetID.getText().trim();
        String qtyStr = txtNewThreshold.getText().trim();

        // 2. EXCEPTION FLOW & CONSTRAINTS: Validate the input
        try {
            int targetId = Integer.parseInt(targetIdStr);
            int newQty = Integer.parseInt(qtyStr);

            // Constraint: Must be a positive integer
            if (newQty < 0) {
                JOptionPane.showMessageDialog(this, "Invalid Input: Threshold cannot be negative.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return; // Stops the process
            }

            // 3. Determine if this is a Category Bulk Update or Single Product
            boolean isCategory = updateType.equals("Entire Category ID");

            // 4. Send to Database
            DatabaseManager db = new DatabaseManager();
            boolean success = db.updateLowStockThreshold(isCategory, targetId, newQty);

            if (success) {
                JOptionPane.showMessageDialog(this, "Thresholds successfully updated!", "Success", JOptionPane.INFORMATION_MESSAGE);
                // Clear the fields after success
                txtTargetID.setText("");
                txtNewThreshold.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Update failed. Please check if the ID exists.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException ex) {
            // Exception Flow: Triggers if they type text (like "abc") instead of a number
            JOptionPane.showMessageDialog(this, "Invalid Input: Please enter valid whole numbers only.", "Input Error", JOptionPane.ERROR_MESSAGE);
        }  // TODO add your handling code here:
    }//GEN-LAST:event_btnSaveThresholdActionPerformed

