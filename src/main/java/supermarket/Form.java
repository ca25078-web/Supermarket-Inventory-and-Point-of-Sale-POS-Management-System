    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    // 1. Fetch the calculated numbers from the database
        DatabaseManager db = new DatabaseManager();
        double[] data = db.getProfitAnalysisData();

        // 2. Format the numbers to 2 decimal places
        String revenueStr = String.format("RM %.2f", data[0]);
        String expensesStr = String.format("RM %.2f", data[1]);
        String profitStr = String.format("RM %.2f", data[2]);
        String marginStr = String.format("%.2f %%", data[3]);

        // 3. Update the labels on your screen!
        // (Make sure to use the exact variable names you set in Step 2)
        lblRevenue.setText("Total Revenue: " + revenueStr);
        lblExpenses.setText("Total Expenses: " + expensesStr);
        
        // Let's make the margin label show the actual profit amount AND the percentage
        lblMargin.setText("Net Profit: " + profitStr + " (Margin: " + marginStr + ")");
        
        // Optional: Make it green if you are making money, red if losing money
        if (data[2] >= 0) {
            lblMargin.setForeground(new java.awt.Color(34, 139, 34)); // Green
        } else {
            lblMargin.setForeground(java.awt.Color.RED);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

